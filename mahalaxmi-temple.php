                <p>
                    The Shri Ambabai (mahalaxmi)Temple of Kolhapur in Maharashtra, India, is one of the most famous temples of Hinduism of the state. Kolhapur and the temple is located on the banks of the Panchganga River, it is about 230km south of Pune.Temple of the goddess Ambabai was built during Chalukya reign. Mounted on a stone platform, the murti of the crowned goddess is made of gemstone and weighs about 40 kilograms. The image of Mahalakshmi carved in black stone is 3 feet in height. The Shri Yantra is carved on one of the walls in the temple. A stone lion (the vahana of the goddess), stands behind the statue. The crown contains an image of the Sheshnag, the serpent of Vishnu.

                    In her four hands, the deity of Mahalakshmi holds objects of symbolic value. The lower right hand holds a mhalunga (a citrus fruit), in the upper right, a large mace (kaumodaki) the mace of Vishnu with its head touching the ground, in the upper left a shield and in the lower left, a bowl (panpatra). Unlike most Hindu sacred images, which face north or east, the deity faces West (Pashchim). There is a small open window on the western wall, through which the light of the setting sun falls on the face of the image for three days around the 21st of each March and September.

                    There are a number of other shrines in the courtyard to the Navagrahas, Surya, Mahishasuramardini, Vitthal-Rakhmai, Shiva, Vishnu, Tulja Bhavani and others. Some of these images date back to the 11th century, while some are of recent origin. Also located in the courtyard is the temple tank “Manikarnika Kund”, on whose bank is a shrine to Visweshwar Mahadev.

                    Kirnotsav (lit. festival of Sun Rays) is celebrated in the Mahalakshmi Temple at Kolhapur, when the rays of the sun fall directly on the deity at the time of sunset. It is said that the Sun god pays his homage to Mahalakshmi Ambabai for three days in a year. This will be on the occasion of “Rath Saptami”.

            
