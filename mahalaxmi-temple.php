                    <h4>
                        <p>  The Shri Mahalakshmi Temple of Kolhapur in Maharashtra, India, is one of the Shakti Peethas also called Dakshin Kashi, listed in various puranas of Hinduism. According to these writings, a shakti peetha is a place associated with Shakti, the goddess of power. </p>
                        The Kolhapur peetha is of special religious significance, being one of the six places where it is believed one can either obtain salvation from desires or have them fulfilled.<p> The temple takes its name from Mahalakshmi, the consort of Vishnu, and it is believed that the divine couple reside in the area.

The temple belongs, architecturally, to the Kannada Chalukya empire, and may have been first built circa 700 AD. Mounted on a stone platform, the image of the four armed and crowned goddess is made of gemstone and weighs about 40 kilograms. </p>The image of Mahalakshmi carved in black stone is 3 feet in height. The Shri yantra is carved on one of the walls in the temple. A stone lion, the vahana of the goddess, stands behind the statue.
